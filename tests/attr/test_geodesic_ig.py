def test_time_forward_tunnel(
    forward_func,
    multiply_by_inputs,
    inputs,
    baselines,
    target,
    additional_forward_args,
    n_neighbors,
    n_steps,
    n_steiner,
    method,
    internal_batch_size,
    return_convergence_delta,
    distance,
    show_progress,
    fails,
):
    with pytest.raises(Exception) if fails else nullcontext():
        explainer = GeodesicIntegratedGradients(forward_func=forward_func, multiply_by_inputs=multiply_by_inputs)

        attr = explainer.attribute(
            inputs=inputs,
            baselines=baselines,
            target=target,
            additional_forward_args=additional_forward_args,
            n_neighbors=n_neighbors,
            n_steps=n_steps,
            n_steiner=n_steiner,
            method=method,
            internal_batch_size=internal_batch_size,
            return_convergence_delta=return_convergence_delta,
            distance=distance,
            show_progress=show_progress,
        )

        if return_convergence_delta:
            attr, delta = attr
            assert tuple(delta.shape) == (8,)

        assert attr.shape == inputs.shape
